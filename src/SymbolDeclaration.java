public class SymbolDeclaration {
    Symbol symbol;  // 符号
    int line;               // 所在行号
    int position;           // 在行中的位置

    public SymbolDeclaration(Symbol symbol, int line, int position) {
        this.symbol = symbol;
        this.line = line;
        this.position = position;
    }

    @Override
    public String toString() {
        return String.format("%-12s line %-4d pos %-4d\n",
                symbol.toString(), line, position);
    }
}
